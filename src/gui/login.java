    private void BuscarUser() {
         conect = new Conexion("gimnasio");
        if (jtfUser.getText().equals("") || jpfPass.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Error\ncampos Vacios.", null, JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                String senten = "SELECT * FROM usuarios WHERE user LIKE '"+jtfUser.getText() +"'";
                encontrado = "NO";
                con = conect.getConexion();
                st = con.createStatement();
                ResultSet rs = st.executeQuery(senten);

                while (rs.next()) {
                        if (jpfPass.getText().equals(rs.getString("pass"))) {

                            JOptionPane.showMessageDialog(null, "Bienvenido " + jtfUser.getText());
                            this.dispose();
                            new Principal(jtfUser.getText()).setVisible(true);
                            encontrado = "SI";
                           
                        } else {
                            JOptionPane.showMessageDialog(null, "Contrase√±a incorrecta");
                        }
                    
                    encontrado = "SI";
                    limpiar();
                }
                if (encontrado.equals("NO")) {
                    JOptionPane.showMessageDialog(null, "USUARIO INCORRECTO", "ATENCION!", JOptionPane.ERROR_MESSAGE);
                }

                con.close();
            } catch (HeadlessException | SQLException x) {
                System.out.println(x);
            }
        }
    }
    
    private void limpiar(){
        jtfUser.setText("");
        jpfPass.setText("");
    }

